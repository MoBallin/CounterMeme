<p>Countermeme is NOT a meme-sharing site. It is a battle for supremacy in which memes are your weapons.</p> 
<br>
<p>The main action of the site takes place in the Smackdowns, which pit two opposing views against each other. Post memes to the side you support, either to argue in favor of your view or against the opposing view. If you come across a meme you find to be woefully ignorant, then click on that meme's "counter" button to reply with a meme of your own. You may also cast one vote per Smackdown toward the side whose memes you find most convincing. The votes are tracked via the scoreboard on each Smackdown and indicate which side currently has the edge.</p>
<br>
<p>Users also get their own "portfolio" where all their created memes are displayed and upvoted memes are stored. Users attain different "ranks" based on the cumulative total of upvotes their memes have received.</p>
